package com.example.cakecontest;

public class Player {

    private String name;
    private String secName;
    private String gender;
    private int age;
    private boolean oldPlayer;
    private String oldContest;

    public Player(String name, String secName, String gender, int age,  boolean oldPlayer, String oldContest) {
        this.name = name;
        this.secName = secName;
        this.gender = gender;
        this.age = age;
        this.oldPlayer = oldPlayer;
        this.oldContest = oldContest;
    }


}
